#include "libft.h"

int		ft_atoi(const char *str)
{
	int res;
	int i;
	int neg;

	neg = 1;
	res = 0;
	i = 0;
	if (str[0] == '\0')
		return (0);
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	res = res * neg;
	return (res);
}
